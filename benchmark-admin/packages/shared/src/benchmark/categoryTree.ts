// V3 category dimension — ported verbatim from legacy frontend/src/data/questionTypeOptions.ts.
// This is the single source of truth for the valid l1→l2→l3 category values and each
// leaf's definition/intent. Categories are free TEXT at the DB layer (KTD1); validity is
// enforced here in the UI tree, not by a DB constraint. Keep the tree content byte-faithful
// to legacy — it is the parity contract between admin and legacy.

export interface CategoryOption {
  value: string;
  label: string;
  code?: string;
  definition?: string;
  children?: CategoryOption[];
}

function node(
  code: string,
  value: string,
  definition = '',
  children?: CategoryOption[],
): CategoryOption {
  return {
    value,
    label: code ? `${code} ${value}` : value,
    code,
    definition,
    ...(children ? { children } : {}),
  };
}

