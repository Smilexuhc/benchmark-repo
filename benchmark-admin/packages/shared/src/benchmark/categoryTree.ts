function findNode(
  categoryL1: string,
  categoryL2?: string,
  categoryL3?: string,
): CategoryOption | undefined {
  const l1 = CATEGORY_TREE.find((o) => o.value === categoryL1);
  if (!l1 || categoryL2 === undefined) return l1;
  const l2 = l1.children?.find((o) => o.value === categoryL2);
  if (!l2 || categoryL3 === undefined) return l2;
  return l2.children?.find((o) => o.value === categoryL3);
}

// The l3 leaf at the given path, or undefined if the path is incomplete/unknown.
export function leafByPath(
  categoryL1: string,
  categoryL2: string,
  categoryL3: string,
): CategoryOption | undefined {
  return findNode(categoryL1, categoryL2, categoryL3);
}

// The selected leaf's definition, or '' for an unknown/incomplete path.
export function definitionFor(categoryL1: string, categoryL2: string, categoryL3: string): string {
  return findNode(categoryL1, categoryL2, categoryL3)?.definition || '';
}

// Display labels [l1, l2, l3] for a full path, or undefined if any level is unknown.
export function labelsForPath(
  categoryL1: string,
  categoryL2: string,
  categoryL3: string,
): [string, string, string] | undefined {
  const l1 = findNode(categoryL1);
  const l2 = findNode(categoryL1, categoryL2);
  const l3 = findNode(categoryL1, categoryL2, categoryL3);
  return l1 && l2 && l3 ? [l1.label, l2.label, l3.label] : undefined;
}
